# Datatypes in Python
# 1. Integer
x=10
# 2. Float
y=10.5
# 3. String
name="Shivraj"
# 4. Boolean
is_student=True
# 5. List
my_list=[1, 2, 3, 4, 5]
# 6. Tuple
my_tuple=(1, 2, 3)
# 7. Dictionary
my_dict={"name": "Shivraj", "age": 18}
# 8. Set
my_set={1, 2, 3, 4, 5}
# 9. NoneType
my_none=None

# Additional data types not commonly used but available in Python:


# 10. Complex
my_complex=3 + 4j
# 11. Bytes
my_bytes=b"Hello"
# 12. Bytearray
my_bytearray=bytearray(5)  # Creates a bytearray of size 5
# 13. Memoryview
my_memoryview=memoryview(bytes(5))  # Creates a memoryview of a bytes object of size 5
# 14. Frozenset
my_frozenset=frozenset([1, 2, 3, 4, 5])  # Creates an immutable set
# 15. Range
my_range=range(5)  # Creates a range object from 0 to 4