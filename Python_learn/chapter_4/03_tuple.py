#  Learning about the tuple
#  tuple is similar to the list, but it is immutable
#  we cannot change the elements of a tuple after it is created

x=(23.42, 45, "hi", 2.5, True)
print(x)

# printing the tuple with the one element
y=(23,)  # note the comma, otherwise it will be treated as an int
print(y)

# else it will be treated as a string